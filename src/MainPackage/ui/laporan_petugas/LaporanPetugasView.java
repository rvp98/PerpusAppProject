    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        int bulan = jmcBulan.getMonth() + 1;
        int tahun = jycTahun.getYear();
        
        try {
            Connection conn = (Connection) MainPackage.Config.configDB();
            JasperDesign jd = JRXmlLoader.load("./src/MainPackage/ui/laporan_petugas/laporanPetugas.jrxml");
            JRDesignQuery query = new JRDesignQuery();
            String sql = "SELECT " +
            " p.`nama_petugas`, " +
            " p.`jk_petugas`, " +
            " p.`no_telp_petugas`, " +
            " p.`alamat_petugas`, " +
            " COUNT(pj.`id_petugas`) AS jml_transaksi " +
            "FROM " +
            " petugas AS p, " +
            " peminjaman AS pj " +
            "WHERE p.`id_petugas` = pj.`id_petugas` " +
            " AND MONTH(pj.`tanggal_pinjam`) = '"+bulan+"' " +
            " AND YEAR(pj.`tanggal_pinjam`) = '"+tahun+"' " +
            "GROUP BY pj.`id_petugas`";
            query.setText(sql);
            jd.setQuery(query);
            JasperReport jr = JasperCompileManager.compileReport(jd);
            JasperPrint jp = JasperFillManager.fillReport(jr, null, conn);
            JasperViewer.viewReport(jp, false);
        } catch (JRException | SQLException ex) {
            Logger.getLogger(LaporanPeminjamanView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCetakActionPerformed


