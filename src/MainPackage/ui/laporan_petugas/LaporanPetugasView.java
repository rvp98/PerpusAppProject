    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Petugas");
        model.addColumn("Jenis Kelamin");
        model.addColumn("No Telp");
        model.addColumn("Alamat");
        model.addColumn("Jumlah Transaksi");
        
        try {
            int no = 1;
            String sql = "SELECT " +
            " p.`nama_petugas`, " +
            " p.`jk_petugas`, " +
            " p.`no_telp_petugas`, " +
            " p.`alamat_petugas`, " +
            " COUNT(pj.`id_petugas`) AS jml_transaksi " +
            "FROM " +
            " petugas AS p, " +
            " peminjaman AS pj " +
            "WHERE p.`id_petugas` = pj.`id_petugas` " +
            " AND YEAR(pj.`tanggal_pinjam`) = YEAR(NOW()) " +
            " AND MONTH(pj.`tanggal_pinjam`) = MONTH(NOW()) " +
            "GROUP BY pj.`id_petugas`";
            java.sql.Connection conn = (Connection) MainPackage.Config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    no++,
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5)
                });
            }
            tblLaporanPetugas.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

